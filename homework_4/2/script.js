var r = /(\+375)[\s\(]?(29|33|44|25)[\s\)]?\s?([0-9]{3})[\s-]?([0-9]{2})[\s-]?([0-9]{2})/;
// var str = "+37529 456-65-65";
var str = prompt("Введите строку с номером телефона");
if(str.match(r) != null){
	var arr = str.match(r).slice(1);
	alert("Номер телефона: " + arr.join(""));
}
else{
	alert("Номер не найден");
}